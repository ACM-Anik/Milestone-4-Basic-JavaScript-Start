// Add some value to the previous/existing value 
var price1 = 32;
var price2 = 10;

price1 = price1 + 10;
// console.log(price1);

// Addition to the same variable shorthand on previous one
//( += means adding some value to the existing value)
price1 += 10;
console.log(price1);


price2 = price2 - 5;
//variable shorthand :
// (-=) means deducting some value from the current value of the variable
price2 -= 5;
console.log(price1);

// *=
price1 *= 5;
console.log(price1);

// /=
price1 /= 5;
console.log(price1);


